/**
 * 업비트에서 거래 가능한 코인인지 확인
 */
export function isUpbitCoin(symbol: string): boolean {
  return UPBIT_COINS.has(symbol);
}

/**
 * 업비트 심볼 생성 (KRW 페어)
 */
export function getUpbitSymbol(symbol: string): string | null {
  return isUpbitCoin(symbol) ? `KRW-${symbol}` : null;
}
